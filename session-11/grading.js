var per = parseFloat (prompt ("enter your precenteg"))

if(per>=80 && per<=100)
{
    document.write("grade-a+")
}

else if(per>=70 && per < 80)
{
    document.write("grade-a")
}

else if(per>=60 && per < 70)
{
    document.write("grade-b")
}

else if(per>=40 && per < 60)
{
    document.write("grade-c")
}

else
 {
    document.write("fail")
 }







